def calculate_final_score_standalone(city_name, params):
    print(f"\n--- Расчет финального скоринга для города ---")

    weighted_score = 0
    final_score_components = {}

    formula: (weighted_score / sum_of_weights) * MAX_POSSIBLE_FINAL_SCORE
    sum_of_weights = sum(FINAL_SCORE_WEIGHTS.values())
    if not (0.99 <= sum_of_weights <= 1.01):
        print(f"Внимание: Сумма весов ({sum_of_weights:.2f}) не равна 1.0. Финальный балл будет масштабирован относительно этой суммы.")

    for param_name, weight in FINAL_SCORE_WEIGHTS.items():
        # Если параметра нет во входных данных, используем 0
        param_value = params.get(param_name, 0)

        # Защита от NaN, если вдруг придут
        if pd.isna(param_value):
            param_value = 0

        # Нормализация
        if param_name == "mean_tci":
            normalized_value = param_value / 100.0 # TCI уже из 100
        else:
            max_val = max_values_for_norm.get(param_name, 1) # Если нет макс. значения, делим на 1
            normalized_value = min(param_value / max_val, 1.0) # Ограничиваем сверху 1.0 (чтобы не было >100%)

        # Вычисляем вклад компонента
        component_score = normalized_value * weight * MAX_POSSIBLE_FINAL_SCORE
        weighted_score += component_score
        final_score_components[param_name] = round(component_score, 2)

    final_city_score = weighted_score

    print(f"\nФИНАЛЬНЫЙ СКОРИНГ для {city_name}: {final_city_score:.2f} из {MAX_POSSIBLE_FINAL_SCORE}")
    print("Детализация по компонентам:")
    for k, v in final_score_components.items():
        print(f"  - {k}: {v:.2f}")

    return final_city_score, final_score_components

def save_final_score(city_name, final_score, file_path=RESULTS_FILE):
    """Сохраняет только название города и финальный балл"""
    ensure_persistent_storage()
    row = pd.DataFrame([{
        'city_name': city_name.strip(),
        'final_score': round(final_score, 2),
        'timestamp': pd.Timestamp.now().strftime('%Y-%m-%d %H:%M')
    }])

    if os.path.exists(file_path):
        existing_df = pd.read_csv(file_path)
        existing_df = existing_df[existing_df['city_name'].fillna('').str.strip() != city_name.strip()]
        updated_df = pd.concat([existing_df, row], ignore_index=True)
    else:
        updated_df = row

    updated_df.to_csv(file_path, index=False, encoding='utf-8-sig')
    print(f"{city_name}: {final_score:.2f} → сохранено в '{file_path}'")

def show_ranking(file_path=RESULTS_FILE, top_n=None):
    ensure_persistent_storage()
    """Показывает отсортированный рейтинг городов"""
    if not os.path.exists(file_path):
        print(f"Файл '{file_path}' пуст или не найден")
        return None

    df = pd.read_csv(file_path)
    df = df.sort_values('timestamp').drop_duplicates('city_name', keep='last')
    df = df.sort_values('final_score', ascending=False).reset_index(drop=True)
    df['rank'] = range(1, len(df) + 1)

    display_cols = ['rank', 'city_name', 'final_score']
    if top_n:
        df = df.head(top_n)

    print(f"\n РЕЙТИНГ (всего городов: {len(df)})")
    print(df[display_cols].to_string(index=False))
    return df


