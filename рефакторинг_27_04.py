def add_bathymetry_to_existing_map(m, subset, bounds, opacity=0.4):
    """Добавляет батиметрию как слой на существующую Folium карту m."""
    import numpy as np, matplotlib.pyplot as plt
    from folium.raster_layers import ImageOverlay

    data = np.nan_to_num(subset.values, nan=0.0)
    vmin, vmax = float(subset.min()), 0  # Глубины отрицательные
    norm = (data - vmin) / (vmax - vmin + 1e-9)

    # Blues_r: тёмно-синий = глубоко, светло-голубой = мелко
    rgb = (plt.get_cmap('Blues_r')(norm)[:, :, :3] * 255).astype(np.uint8)

    # Коррекция ориентации, если координаты идут снизу вверх
    if subset.lat.values[0] < subset.lat.values[-1]:
        rgb = rgb[::-1]

    ImageOverlay(
        image=rgb,
        bounds=[[bounds['min_lat'], bounds['min_lon']],
                [bounds['max_lat'], bounds['max_lon']]],
        opacity=opacity,
        name="Батиметрия"
    ).add_to(m)
    return m

