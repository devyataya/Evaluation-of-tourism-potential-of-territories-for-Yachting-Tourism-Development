def plot_radar_with_ring(profile, title="City profile"):
    labels = list(profile.keys())
    values = list(profile.values())
    N = len(labels)

    values += values[:1]
    angles = np.linspace(0, 2*np.pi, N, endpoint=False).tolist()
    angles += angles[:1]

    fig, ax = plt.subplots(figsize=(8, 8), subplot_kw=dict(polar=True))

    # цвета
    ring_colors = [
        "#F0D9EF", "#FCDCE1", "#FFE6BB",  # Инфра, Транспорт, Медицина
        "#E9ECCE", "#CDE9DC", "#C4DFE5",  # Природа, Климат, Туризм
    ]

    for i in range(N):
        ax.bar(
            angles[i], 1.2, width=2*np.pi/N, bottom=1.05,
            color=ring_colors[i], edgecolor="white", linewidth=1
        )

    # радар
    ax.plot(angles, values, linewidth=2, color='#0047AB')
    ax.fill(angles, values, alpha=0.25, color='#1E90FF')

    # подписи (повёрнутые)
    for angle, label in zip(angles[:-1], labels):
        rotation = np.degrees(angle) - 90
        ax.text(angle, 1.14, label, ha='center', va='center',
                fontsize=12, rotation=rotation, rotation_mode='anchor')

    # сетка
    for angle in angles[:-1]:
        ax.plot([angle, angle], [0, 1.0], '-', color='gray', alpha=0.3, linewidth=0.8)

    ax.set_yticks(np.linspace(0.2, 1.0, 5))
    ax.set_yticklabels([])
    ax.set_xticks([])
    ax.set_ylim(0, 1.25)

    # значения на осях
    for angle, value in zip(angles[:-1], values[:-1]):
        ax.text(angle, value - 0.12, f"{value:.2f}",
                ha='center', va='center', fontsize=10, color='black', fontweight='bold',
                bbox=dict(boxstyle='round', facecolor='white', edgecolor='black', alpha=0.8, linewidth=0.5))

    ax.set_title(title, size=14, pad=20)
    plt.tight_layout()
    plt.show()

