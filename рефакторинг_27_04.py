import numpy as np

def analyze_marina_depth_quality(bathy_subset, bathy_bounds, target_depth_min=2.5, target_depth_max=5.0):
    """
    Анализирует пригодность акватории для марин по глубине.

    Параметры:
    - bathy_subset: xarray DataArray с глубиной (отрицательные = вода)
    - target_depth_min: мин. оптимальная глубина в метрах (по умолчанию 2.5 м)
    - target_depth_max: макс. оптимальная глубина в метрах (по умолчанию 5.0 м)

    Returns:
    - dict с метриками:
        * suitable_area_km2: площадь в оптимальном диапазоне глубин
        * suitable_pct: % площади в оптимальном диапазоне
        * mean_depth_m: средняя глубина (по модулю)
        * depth_score: оценка 0–100 (чем ближе к целевому диапазону, тем выше)
    """
    # Глубины в GEBCO отрицательные, берём модуль
    depths = np.abs(np.nan_to_num(bathy_subset.values, nan=np.nan))

    # Площадь одной ячейки (приближённо, для малых областей)
    # 1° широты ≈ 111 км, 1° долготы ≈ 111 км * cos(lat)
    center_lat = (bathy_bounds['min_lat'] + bathy_bounds['max_lat']) / 2
    km_per_deg_lat = 111.0
    km_per_deg_lon = 111.0 * np.cos(np.radians(center_lat))

    # Разрешение сетки в градусах
    dlat = abs(bathy_subset.lat.values[1] - bathy_subset.lat.values[0]) if len(bathy_subset.lat) > 1 else 0.01
    dlon = abs(bathy_subset.lon.values[1] - bathy_subset.lon.values[0]) if len(bathy_subset.lon) > 1 else 0.01

    cell_area_km2 = (dlat * km_per_deg_lat) * (dlon * km_per_deg_lon)

    # Подсчёт ячеек в целевом диапазоне
    suitable_mask = (depths >= target_depth_min) & (depths <= target_depth_max)
    suitable_cells = np.sum(suitable_mask)
    total_cells = np.sum(~np.isnan(bathy_subset.values))

    suitable_area_km2 = suitable_cells * cell_area_km2
    suitable_pct = (suitable_cells / total_cells * 100) if total_cells > 0 else 0

    # Средняя глубина только по воде
    water_mask = depths > 0
    mean_depth = np.mean(depths[water_mask]) if np.any(water_mask) else 0

    # Оценка качества: 100 баллов если 100% площади в диапазоне,
    # линейно снижается если меньше
    depth_score = min(100, suitable_pct * 1.2)  # небольшой бонус за идеальное покрытие

    return {
        'suitable_area_km2': round(suitable_area_km2, 2),
        'suitable_pct': round(suitable_pct, 1),
        'mean_depth_m': round(mean_depth, 2),
        'depth_score': round(depth_score, 1)
    }

