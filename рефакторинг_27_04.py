class ClimateAnalyzer:
    """Класс для анализа сеточных климатических данных и расчета TCI."""

    def __init__(self, nc_file_path):
        """
        Инициализация с путем к NetCDF файлу.
        """
        self.nc_file_path = nc_file_path
        self.data = None
        self.tci_grid = None
        self.comfort_polygons = None
        self._load_and_prepare_data()

    def _load_and_prepare_data(self):
        # 1. Открываем все .nc файлы
        self.data = xr.open_mfdataset(self.nc_file_path, engine='h5netcdf', combine='by_coords')

        # 2. Убираем измерение 'expver', если оно есть (частая причина ошибок в ERA5)
        if 'expver' in self.data.dims:
            self.data = self.data.mean(dim='expver', skipna=True)

        # Также проверяем 'valid_time' vs 'time'
        if 'valid_time' in self.data.dims:
            self.data = self.data.rename({'valid_time': 'time'})

        # 3. Определяем правильные имена координат
        # ERA5 может использовать 'longitude'/'latitude' или 'lon'/'lat'
        x_dim = 'longitude' if 'longitude' in self.data.coords else 'lon'
        y_dim = 'latitude' if 'latitude' in self.data.coords else 'lat'

        print(f"Используемые пространственные измерения: X={x_dim}, Y={y_dim}")

        # 4. Настраиваем пространственную привязку для rioxarray
        self.data = self.data.rio.set_spatial_dims(x_dim=x_dim, y_dim=y_dim, inplace=True)
        self.data = self.data.rio.write_crs("EPSG:4326", inplace=True)

        # 5. Расчет ветра
        if 'u10' in self.data and 'v10' in self.data:
            self.data['wind_speed'] = (self.data['u10']**2 + self.data['v10']**2)**0.5
        elif 'si10' in self.data:
            self.data['wind_speed'] = self.data['si10']
        else:
            print("Предупреждение: Не удалось найти переменные скорости ветра.")
            self.data['wind_speed'] = xr.zeros_like(self.data['t2m']).astype(float)

        # 6. Конвертация величин
        self.data['t2m_celsius'] = self.data['t2m'] - 273.15
        self.data['dewpoint_celsius'] = self.data['d2m'] - 273.15
        self.data['precipitation_hours'] = self.data['tp'] * (24 * 30)
        self.data['cloud_cover_percent'] = self.data['tcc'] * 100

        t = self.data['t2m_celsius']
        td = self.data['dewpoint_celsius']
        self.data['relative_humidity'] = 100 * (
            (112 - 0.1 * t + td) / (112 + 0.9 * t)
        ).clip(0, 100)

        # 7. Усреднение по времени
        time_dim = 'time' if 'time' in self.data.dims else 'valid_time'
        self.climatic_map = self.data[['t2m_celsius', 'wind_speed', 'relative_humidity', 'precipitation_hours', 'cloud_cover_percent']].mean(dim=time_dim, skipna=True)

        # 8. Важно: Снова применяем rio настройки для результирующей карты (climatic_map)
        # Так как усреднение могло сбросить пространственные атрибуты
        self.climatic_map = self.climatic_map.rio.set_spatial_dims(x_dim=x_dim, y_dim=y_dim, inplace=True)
        self.climatic_map = self.climatic_map.rio.write_crs("EPSG:4326", inplace=True)

    def calculate_tci_grid(self):
        # 1. Температурный комфорт
        C_temp = (MAX_SCORE - abs(self.climatic_map["t2m_celsius"] - PERFECT_TEMP) / TEMP_RANGE).clip(min=0)

        # 2. Осадки
        C_prec = (MAX_SCORE - self.climatic_map["precipitation_hours"] / PRECIPITATION_IMPACT).clip(min=0)

        # 3. Облачность
        C_cloud = (MAX_SCORE - self.climatic_map["cloud_cover_percent"] / CLOUDCOVER_IMPACT).clip(min=0)

        # 4. Ветер
        C_wind = (MAX_SCORE - abs(self.climatic_map["wind_speed"] - PERFECT_WIND) / WIND_INCLINATION).clip(min=0)

        # 5. Влажность ← НОВОЕ
        C_humid = (MAX_SCORE - abs(self.climatic_map["relative_humidity"] - PERFECT_HUMID) / HUMIDITY_RANGE).clip(min=0)

        # 6. Итоговый TCI (теперь с 5 компонентами, как в оригинальной формуле)
        tci_raw = (
            WEIGHT_DAY * C_temp +
            WEIGHT_SUTKI * C_temp +      # Используем ту же температуру для суток
            WEIGHT_PREC * C_prec +
            WEIGHT_CLOUD * C_cloud +
            WEIGHT_WIND * C_wind +
            WEIGHT_HUMID * C_humid       # ← НОВОЕ: вес влажности
        )

        # Нормализация
        tci_max_possible = (WEIGHT_DAY + WEIGHT_SUTKI + WEIGHT_PREC + WEIGHT_CLOUD + WEIGHT_WIND + WEIGHT_HUMID) * MAX_SCORE
        self.tci_grid = (tci_raw / tci_max_possible * 100).fillna(0)
        print("Карта TCI успешно рассчитана (с влажностью).")

    def create_comfort_polygons(self, tci_threshold=52):
        """
        Создает полигоны из ячеек, где TCI выше заданного порога.
        """
        from shapely.geometry import shape # <-- ДОБАВИЛИ ИМПОРТ ПЕРЕВОДЧИКА ЗДЕСЬ

        # print(f"Создаем полигоны для зон с TCI > {tci_threshold}...")

        comfort_raster = self.tci_grid.where(self.tci_grid >= tci_threshold)

        results = list(shapes(
            comfort_raster.values,
            mask=comfort_raster.notnull().values,
            transform=comfort_raster.rio.transform()
        ))

        if not results:
            print("Не найдено зон, удовлетворяющих климатическим критериям.")
            self.comfort_polygons = gpd.GeoDataFrame(geometry=[], crs=self.data.rio.crs)
            return

        geometries = [shape(r[0]) for r in results]
        self.comfort_polygons = gpd.GeoDataFrame(geometry=geometries, crs=self.data.rio.crs)
        print(f"Найдено {len(self.comfort_polygons)} полигонов с комфортным климатом.")

