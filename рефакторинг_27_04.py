def get_bathymetry_for_city(ds, city_name, buffer_km=50):
    """Вырезает батиметрию вокруг города."""
    try:
        gdf = ox.geocode_to_gdf(city_name)
        center = gdf.geometry.centroid.iloc[0]
        lon, lat = center.x, center.y
    except Exception as e:
        print(f"Ошибка геокодирования '{city_name}': {e}")
        return None, None

    width_km = 50   # по долготе (восток-запад)
    height_km = 30   # по широте (север-юг) ← меняй тут, если нужно уже/шире

    deg_lon = width_km / 111.0 / np.cos(np.radians(lat))
    deg_lat = height_km / 111.0

    min_lon, max_lon = lon - deg_lon, lon + deg_lon
    min_lat, max_lat = lat - deg_lat, lat + deg_lat

    f_lon_min, f_lon_max = float(ds.lon.min()), float(ds.lon.max())
    f_lat_min, f_lat_max = float(ds.lat.min()), float(ds.lat.max())

    if (max_lon < f_lon_min or min_lon > f_lon_max or
        max_lat < f_lat_min or min_lat > f_lat_max):
        print("Город вне покрытия файла GEBCO!")
        return None, None

    subset = ds.elevation.sel(
        lat=slice(max(min_lat, f_lat_min), min(max_lat, f_lat_max)),
        lon=slice(max(min_lon, f_lon_min), min(max_lon, f_lon_max))
    )

    bounds = {
        'min_lon': max(min_lon, f_lon_min), 'max_lon': min(max_lon, f_lon_max),
        'min_lat': max(min_lat, f_lat_min), 'max_lat': min(max_lat, f_lat_max)
    }
    print(f"Вырезано: {subset.sizes['lat']} × {subset.sizes['lon']} ячеек")
    return subset, bounds

def plot_bathymetry(subset, bounds, title="Bathymetry"):
    fig, ax = plt.subplots(figsize=(10, 7))
    elevation = subset.where(subset <= 0)  # только вода
    im = elevation.plot(ax=ax, cmap='Blues_r', robust=True, add_colorbar=True,
                        cbar_kwargs={'label': 'Depth (m)'},
                        vmin=float(elevation.min()), vmax=0)
    ax.set_title(f"{title}\n{bounds['min_lat']:.2f}°–{bounds['max_lat']:.2f}°, "
                 f"{bounds['min_lon']:.2f}°–{bounds['max_lon']:.2f}°")
    ax.set_xlabel("Longitude"); ax.set_ylabel("Latitude"); ax.grid(alpha=0.3)
    plt.tight_layout(); plt.show()
    print(f"\nСтатистика: Мин={subset.min().values.item():.1f} м | "
          f"Макс={subset.max().values.item():.1f} м | Среднее={subset.mean().values.item():.1f} м")

