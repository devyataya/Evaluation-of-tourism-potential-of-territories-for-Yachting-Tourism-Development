def get_climate_bbox(city_name, size_km=50):
    """
    Возвращает квадратный Bounding Box [N, W, S, E] заданного размера (в км).
    Автоматически корректирует долготу, чтобы область не была вытянутой по вертикали.
    """
    try:
        gdf = ox.geocode_to_gdf(city_name)
        center = gdf.geometry.centroid.iloc[0]
        lat, lon = center.y, center.x

        # 1° широты ≈ 111.32 км
        half_deg_lat = (size_km / 2) / 111.32

        # 1° долготы ≈ 111.32 * cos(lat) км. Добавляем eps для защиты у полюсов
        cos_lat = max(np.cos(np.radians(lat)), 0.01)
        half_deg_lon = (size_km / 2) / (111.32 * cos_lat)

        north = min(90.0, lat + half_deg_lat)
        south = max(-90.0, lat - half_deg_lat)
        west  = max(-180.0, lon - half_deg_lon)
        east  = min(180.0, lon + half_deg_lon)

        print(f"Центр '{city_name}': {lat:.4f}°, {lon:.4f}°")
        print(f"Область {size_km}×{size_km} км → [N={north:.3f}, W={west:.3f}, S={south:.3f}, E={east:.3f}]")
        return [north, west, south, east]

    except Exception as e:
        print(f"Ошибка геокодирования '{city_name}': {e}")
        return [56.0, 37.0, 55.0, 38.0]  # Fallback: Москва

