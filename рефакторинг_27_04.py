class CityAnalyzer: #Класс для анализа города по геоданным.
    def __init__(self, data_handler):
        self.data_handler = data_handler
        self.buffers_wgs84 = {}
        self.infrastructure_counts = {}
        self.tourist_gdfs = {}
        self.df_tourist = None
        self.coastal_length = 0
        self.total_area = 0

    def calculate_parameters(self): # Рассчитывает все параметры для анализа города
        self._calculate_coastal_length()
        self._convert_buffers_to_wgs84()
        self._calculate_water_area()
        if self.load_analysis_cache():
            return
        self._count_infrastructure()
        self._find_tourist_objects()
        self.save_analysis_cache()

    def _get_analysis_cache_path(self, folder=ANALYSIS_CACHE_FOLDER):
        safe_name = self.data_handler.place_name.replace(',', '_').replace(' ', '_').replace('/', '_').lower()
        return os.path.join(folder, f"{safe_name}_analysis.json")

    def save_analysis_cache(self, folder=ANALYSIS_CACHE_FOLDER):
        ensure_persistent_storage()
        cache_payload = {
            "buffer_names": sorted(self.buffers_wgs84.keys()),
            "infrastructure_counts": self.infrastructure_counts,
            "coastal_length": float(self.coastal_length),
            "total_area": float(self.total_area),
            "df_tourist": [] if self.df_tourist is None else self.df_tourist.to_dict(orient="records")
        }
        cache_path = self._get_analysis_cache_path(folder)
        with open(cache_path, 'w', encoding='utf-8') as f:
            json.dump(convert_numpy_types(cache_payload), f, ensure_ascii=False, indent=2)
        print(f"Кэш анализа города сохранен: {cache_path}")

    def load_analysis_cache(self, folder=ANALYSIS_CACHE_FOLDER):
        ensure_persistent_storage()
        cache_path = self._get_analysis_cache_path(folder)
        if not os.path.exists(cache_path):
            return False

        try:
            with open(cache_path, 'r', encoding='utf-8') as f:
                cache_payload = json.load(f)

            expected_buffers = sorted(self.buffers_wgs84.keys())
            cached_buffers = sorted(cache_payload.get("buffer_names", []))
            if expected_buffers != cached_buffers:
                print("Кэш анализа найден, но набор буферов изменился. Пересчитываю анализ.")
                return False

            self.infrastructure_counts = cache_payload.get("infrastructure_counts", {})
            self.coastal_length = float(cache_payload.get("coastal_length", self.coastal_length))
            self.total_area = float(cache_payload.get("total_area", self.total_area))

            tourist_records = cache_payload.get("df_tourist", [])
            self.df_tourist = pd.DataFrame(tourist_records)
            if not self.df_tourist.empty:
                numeric_cols = self.df_tourist.select_dtypes(include=['float', 'int']).columns
                self.df_tourist[numeric_cols] = self.df_tourist[numeric_cols].fillna(0).astype(int)

            print(f"Кэш анализа города загружен: {cache_path}")
            return True
        except Exception as e:
            print(f"Не удалось загрузить кэш анализа: {e}")
            return False

    def process_data(self, dataset_filtered=None): # Обрабатывает данные и выводит результаты анализа.
        print(f"\nАнализ города: {self.data_handler.place_name}...\n")
        self._print_natural_factors()
        self._print_infrastructure_counts()
        self._print_tourist_info()

    def _calculate_coastal_length(self): # Вычисляет длину береговой линии
        try: self.coastal_length = self.data_handler.coastline_proj.geometry.length.sum() if self.data_handler.coastline_proj is not None else 0
        except: self.coastal_length = 0

    def _convert_buffers_to_wgs84(self):
        for distance_m, buffer_proj in self.data_handler.buffers_proj.items():
            try: self.buffers_wgs84[f'{distance_m / 1000:.0f}km'] = self.data_handler._buffer_to_wgs84(buffer_proj)
            except: pass

    def _calculate_water_area(self):
        buffer_5km_proj = self.data_handler.buffers_proj.get(BUFFER, None)
