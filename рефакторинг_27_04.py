def convert_numpy_types(obj):
    """Рекурсивно преобразует numpy-типы в стандартные типы Python для JSON."""
    if isinstance(obj, dict):
        return {k: convert_numpy_types(v) for k, v in obj.items()}
    elif isinstance(obj, list):
        return [convert_numpy_types(i) for i in obj]
    elif isinstance(obj, np.integer):
        return int(obj)
    elif isinstance(obj, np.floating):
        return float(obj)
    elif isinstance(obj, np.ndarray):
        return obj.tolist()
    else:
        return obj

class DataHandler: # Класс для загрузки и обработки геоданных города

    def __init__(self, place_name): #Инициализация класса DataHandler
        self.place_name = place_name
        self.city_name = None
        self.country_name = None
        self.place_gdf = None
        self.marinas = None
        self.coastline = None
        self.coastline_proj = None
        self.buffers_proj = {}
        self.islands_gdf = None
        self.marine_reserves = None
        self.marine_reserves_proj = None

        if ',' in place_name:
            self.city_name, self.country_name = [x.strip() for x in place_name.split(',', 1)]
        else:
            self.city_name = place_name.strip()
            self.country_name = None

    def load_data(self, use_cache=True):
        """Загружает данные: сначала пробует кэш, потом OSM."""
        print(f"\nЗагрузка данных для города: {self.place_name}...\n")
        self._load_place_geometry()

        # Пробуем загрузить из кэша
        if use_cache and self.load_geodata():
            if self.coastline is None or self.coastline.empty:
                print("Кэш найден, но береговая линия отсутствует. Пробую определить ее заново...")
                try:
                    self.coastline = self._load_coastline_with_fallback()
                    if self.coastline is not None and not self.coastline.empty:
                        self.coastline_proj = ox.projection.project_gdf(self.coastline)
                    else:
                        self.coastline_proj = None
                except Exception:
                    self.coastline = gpd.GeoDataFrame()
                    self.coastline_proj = None
            return  # Если кэш найден, выходим сразу

        # Если кэша нет — загружаем из OSM
        print("Кэш не найден, загружаю из OpenStreetMap...")
        try:
            self.marinas = self._get_osm_features({'leisure': 'marina'})
        except: self.marinas = gpd.GeoDataFrame()

        try:
            self.coastline = self._load_coastline_with_fallback()
            self.coastline_proj = ox.projection.project_gdf(self.coastline) if self.coastline is not None and not self.coastline.empty else None
        except:
            self.coastline = gpd.GeoDataFrame()
            self.coastline_proj = None

    def _load_place_geometry(self):
        try:
            self.place_gdf = ox.geocode_to_gdf(self.place_name)
        except Exception as e:
            print(f"Не удалось получить геометрию города '{self.place_name}': {e}")
            self.place_gdf = None

    def _load_coastline_with_fallback(self, search_buffer_m=15000):
        coastline = self._get_osm_features({'natural': 'coastline'})
        coastline = self._prepare_coastline_gdf(coastline)
        if coastline is not None and not coastline.empty:
            print("Береговая линия найдена в границе города.")
            return coastline

        place_polygon = self._get_place_polygon()
        if place_polygon is None:
            return gpd.GeoDataFrame()

        buffered_polygon = self._buffer_geometry_in_meters(place_polygon, search_buffer_m)
        if buffered_polygon is not None:
            try:
                coastline = ox.features_from_polygon(buffered_polygon, tags={'natural': 'coastline'})
                coastline = self._prepare_coastline_gdf(coastline, clip_geom=buffered_polygon)
                if coastline is not None and not coastline.empty:
                    print("Береговая линия найдена в расширенной зоне вокруг города.")
                    return coastline
            except Exception as e:
                print(f"Расширенный поиск береговой линии не сработал: {e}")

        derived_coastline = self._derive_coastline_from_water(place_polygon, buffered_polygon)
        if derived_coastline is not None and not derived_coastline.empty:
            print("Береговая линия восстановлена по границе города и водным полигонам.")
            return derived_coastline

        print("Береговая линия не найдена даже после расширенного поиска.")
        return gpd.GeoDataFrame()

    def _get_place_polygon(self):
        if self.place_gdf is None or self.place_gdf.empty:
            return None
        geometry = self.place_gdf.geometry.unary_union
        if geometry is None or geometry.is_empty:
            return None
        return geometry

    def _buffer_geometry_in_meters(self, geometry, distance_m):
        try:
            geom_series = gpd.GeoSeries([geometry], crs=4326).to_crs(3857)
            return geom_series.buffer(distance_m).to_crs(4326).iloc[0]
        except Exception:
            return None

    def _prepare_coastline_gdf(self, gdf, clip_geom=None):
        if gdf is None or gdf.empty or 'geometry' not in gdf:
            return gpd.GeoDataFrame()
        prepared = gdf[gdf.geometry.notnull()].copy()
        if prepared.empty:
            return gpd.GeoDataFrame()
        if clip_geom is not None:
            try:
                prepared = prepared[prepared.geometry.intersects(clip_geom)]
            except Exception:
                pass
        if prepared.empty:
            return gpd.GeoDataFrame()
        return gpd.GeoDataFrame(prepared, geometry='geometry', crs=getattr(prepared, 'crs', 'EPSG:4326'))

    def _filter_marine_features(self, gdf):
        if gdf is None or gdf.empty:
            return gpd.GeoDataFrame()

        filtered = gdf[gdf.geometry.notnull()].copy()
        if filtered.empty:
            return gpd.GeoDataFrame()

        marine_water_values = {'sea', 'bay', 'lagoon', 'strait', 'sound'}
        marine_natural_values = {'bay', 'strait', 'beach', 'shoal'}
        marine_place_values = {'sea', 'ocean'}
        marine_name_markers = [
            'sea', 'ocean', 'bay', 'strait', 'sound',
            'море', 'океан', 'бухт', 'залив', 'пролив',
            'black sea', 'azov sea', 'mediterranean'
        ]

        marine_mask = pd.Series(False, index=filtered.index)

        if 'water' in filtered.columns:
            marine_mask = marine_mask | filtered['water'].astype(str).str.lower().isin(marine_water_values)
        if 'natural' in filtered.columns:
            marine_mask = marine_mask | filtered['natural'].astype(str).str.lower().isin(marine_natural_values)
        if 'place' in filtered.columns:
            marine_mask = marine_mask | filtered['place'].astype(str).str.lower().isin(marine_place_values)

        name_columns = [col for col in ['name', 'name:ru', 'name:en'] if col in filtered.columns]
        for col in name_columns:
            names = filtered[col].astype(str).str.lower()
            for marker in marine_name_markers:
                marine_mask = marine_mask | names.str.contains(marker, na=False)

        filtered = filtered[marine_mask]
        if filtered.empty:
            return gpd.GeoDataFrame()
        return gpd.GeoDataFrame(filtered, geometry='geometry', crs=getattr(filtered, 'crs', 'EPSG:4326'))

    def _derive_coastline_from_water(self, place_polygon, search_polygon=None):
        query_polygon = search_polygon if search_polygon is not None else place_polygon
        water_tags = {
            'natural': ['water', 'bay', 'strait', 'beach'],
            'water': ['sea', 'bay', 'lagoon', 'strait', 'sound'],
            'place': ['sea', 'ocean']
        }
        try:
            water_gdf = ox.features_from_polygon(query_polygon, tags=water_tags)
        except Exception as e:
            print(f"Не удалось загрузить водные полигоны для fallback-логики: {e}")
            return gpd.GeoDataFrame()

        if water_gdf is None or water_gdf.empty or 'geometry' not in water_gdf:
            return gpd.GeoDataFrame()

        try:
            water_gdf = self._filter_marine_features(water_gdf)
            if water_gdf.empty:
                return gpd.GeoDataFrame()

            place_proj = gpd.GeoSeries([place_polygon], crs=4326).to_crs(3857).iloc[0]
            water_proj = water_gdf.to_crs(3857)
            water_union = unary_union([geom for geom in water_proj.geometry if geom is not None and not geom.is_empty])
            shoreline = place_proj.boundary.intersection(water_union)

            if shoreline is None or shoreline.is_empty:
                shoreline = place_proj.intersection(water_union.boundary)
            if shoreline is None or shoreline.is_empty:
                return gpd.GeoDataFrame()

            shoreline_gdf = gpd.GeoDataFrame(geometry=[shoreline], crs=3857).to_crs(4326)
            shoreline_gdf = shoreline_gdf[shoreline_gdf.geometry.notnull() & ~shoreline_gdf.geometry.is_empty]
            return shoreline_gdf
        except Exception as e:
            print(f"Не удалось восстановить береговую линию из водных полигонов: {e}")
            return gpd.GeoDataFrame()

    def create_buffers(self, distances_m): # Создает буферы вокруг береговой линии
        if self.coastline is None or self.coastline.empty: return
        for distance_m in distances_m:
            self.buffers_proj[distance_m] = self._create_buffer_proj(self.coastline, distance_m)

    def find_islands(self, buffer_distance_km=15, max_area_km2=5.0, min_distance_from_mainland_m=500):
        """Определяет острова с фильтрацией урбанизированных."""
        buffer_proj = self.buffers_proj.get(buffer_distance_km * 1000, None)
        if buffer_proj is None or self.coastline is None or self.coastline.empty:
            print("Буфер или береговая линия не найдены.")
            self.islands_gdf = gpd.GeoDataFrame()
            return

        try:
            islands_tags = {'place': ['island', 'islet']}
            self.islands_gdf = ox.features_from_polygon(self._buffer_to_wgs84(buffer_proj), islands_tags)

            if self.islands_gdf.empty:
                print("Острова не найдены.")
                return

            islands_proj = self.islands_gdf.to_crs(3857)
            coastline_proj = self.coastline_proj

            # Фильтр 1: Площадь
            islands_proj['area_km2'] = islands_proj.geometry.area / 1e6
            islands_proj = islands_proj[islands_proj['area_km2'] <= max_area_km2]

            # Фильтр 2: Расстояние от берега
            distances = [coastline_proj.geometry.distance(geom).min() for geom in islands_proj.geometry]
            islands_proj['distance_from_coast_m'] = distances
            islands_proj = islands_proj[islands_proj['distance_from_coast_m'] >= min_distance_from_mainland_m]

            # Фильтр 3: Имена (урбанизированные)
            urban_names = ['каменный', 'елагин', 'крестовский', 'петроградский', 'васильевский', 'декабристов', 'бычий', 'коломенский', 'покровский', 'летний сад', 'монастырский', 'гутуевский', 'канонерский', 'серный']
            if 'name' in islands_proj.columns:
                islands_proj['name_lower'] = islands_proj['name'].str.lower()
                for uname in urban_names:
                    islands_proj = islands_proj[~islands_proj['name_lower'].str.contains(uname, na=False)]
                islands_proj = islands_proj.drop(columns=['name_lower'])

            self.islands_gdf = islands_proj.to_crs(4326)
            self.islands_gdf = self.islands_gdf[self.islands_gdf.geometry.notnull()]
            print(f"Найдено {len(self.islands_gdf)} туристических островов")
        except Exception as e:
            print(f"Ошибка поиска островов: {e}")
            self.islands_gdf = gpd.GeoDataFrame()

    def find_marine_reserves(self): # Находит морские заповедники в пределах города
        try:
            all_protected_areas = ox.features_from_place(self.place_name, tags={'boundary': 'protected_area'})
            if all_protected_areas.empty:
                self.marine_reserves = gpd.GeoDataFrame()
                print("Заповедники не найдены.")
                return

            explicitly_marine = all_protected_areas[
                (all_protected_areas.get('protection_title', '').astype(str).str.contains('marine', case=False, na=False)) |
                (all_protected_areas.get('name', '').astype(str).str.contains('marine', case=False, na=False))
            ].copy()

            nature_reserves = all_protected_areas[all_protected_areas.get('leisure', '') == 'nature_reserve'].copy()
            coastal_nature_reserves = gpd.GeoDataFrame()

            if not nature_reserves.empty and self.buffers_proj.get(BUFFER):
                buffer_5km_proj = self.buffers_proj[BUFFER]
                nature_reserves_proj = nature_reserves.to_crs(buffer_5km_proj.crs)
                coastal_reserves_candidates = gpd.sjoin(nature_reserves_proj, buffer_5km_proj, how="inner", predicate='intersects')

                if not coastal_reserves_candidates.empty:
                    unique_indices = coastal_reserves_candidates.index.unique()
                    coastal_nature_reserves = nature_reserves.loc[unique_indices]

            if not coastal_nature_reserves.empty:
                self.marine_reserves = pd.concat([explicitly_marine, coastal_nature_reserves], ignore_index=True)
            else:
                self.marine_reserves = explicitly_marine

            if not self.marine_reserves.empty:
                self.marine_reserves = self.marine_reserves.drop_duplicates(subset=['osmid'])
                self.marine_reserves = self.marine_reserves[self.marine_reserves.geometry.notnull()]
                self.marine_reserves_proj = ox.projection.project_gdf(self.marine_reserves)
                print(f"Найдено {len(self.marine_reserves)} морских/прибрежных заповедников.")
            else:
                self.marine_reserves = gpd.GeoDataFrame()
                self.marine_reserves_proj = None
                print("Морские/прибрежные заповедники не найдены.")
        except Exception as e:
            print(f"Ошибка поиска заповедников: {e}")
            self.marine_reserves = gpd.GeoDataFrame()
            self.marine_reserves_proj = None

    def _get_osm_features(self, tags):
        try: return ox.features_from_place(self.place_name, tags=tags)
        except: return gpd.GeoDataFrame()

    def _create_buffer_proj(self, gdf, distance_m):
        gdf_proj = ox.projection.project_gdf(gdf)
        buffer_proj = gdf_proj.geometry.buffer(distance_m)
        buffer_gdf_proj = gpd.GeoDataFrame(geometry=[buffer_proj.unary_union], crs=gdf_proj.crs)
        return buffer_gdf_proj

    def _buffer_to_wgs84(self, buffer_gdf_proj):
        return buffer_gdf_proj.to_crs(4326).geometry.iloc[0]

