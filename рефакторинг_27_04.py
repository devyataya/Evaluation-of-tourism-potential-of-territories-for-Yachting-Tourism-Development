    def _find_tourist_objects(self):
        tourist_tags = {'Достопримечательности': {'tourism': ['attraction', 'museum', 'viewpoint', 'artwork']}, 'Археологические': {'historic': ['archaeological_site', 'ruins']}}
        self.tourist_gdfs = {}
        available_buffers = sorted(
            self.buffers_wgs84.keys(),
            key=lambda name: float(name.replace('km', ''))
        )
        search_buffer_name = '5km' if '5km' in self.buffers_wgs84 else (available_buffers[-1] if available_buffers else None)
        query_polygon = self._get_city_coastal_query_polygon(search_buffer_name) if search_buffer_name is not None else None
        for cat, tags in tourist_tags.items():
            try:
                if query_polygon is not None and not query_polygon.is_empty:
                    gdf = ox.features_from_polygon(query_polygon, tags)
                    if not gdf.empty: gdf = ox.projection.project_gdf(gdf).to_crs(4326)
                    self.tourist_gdfs[cat] = gdf
                else: self.tourist_gdfs[cat] = gpd.GeoDataFrame()
            except: self.tourist_gdfs[cat] = gpd.GeoDataFrame()
        try:
            self.df_tourist = self._count_objects_in_buffers(self.tourist_gdfs, self.buffers_wgs84)
            if self.df_tourist is not None and not self.df_tourist.empty:
                numeric_cols = self.df_tourist.select_dtypes(include=['float', 'int']).columns
                self.df_tourist[numeric_cols] = self.df_tourist[numeric_cols].fillna(0).astype(int)
        except: self.df_tourist = pd.DataFrame()

    def _count_objects_in_buffers(self, gdfs_dict, buffers_dict):
        results = []
        for category, gdf in gdfs_dict.items():
            if gdf is None or gdf.empty:
                results.append({"Категория": category, **{name: 0 for name in buffers_dict.keys()}}); continue
            buffer_counts = {}
            for dist_name, buffer_geom in buffers_dict.items():
                try:
                    geom_series = gpd.GeoSeries([buffer_geom]); unioned = geom_series.unary_union
                    buffer_counts[dist_name] = int(gdf.within(unioned).sum())
                except: buffer_counts[dist_name] = 0
            results.append({"Категория": category, **buffer_counts})
        return pd.DataFrame(results)

